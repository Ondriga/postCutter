package postCutter.fe.postcutter.customViews.rectangle;

public class CoordinateFloat {
    private float x;
    private float y;

    public CoordinateFloat(float x, float y) {
        this.x = x;
        this.y = y;
    }

    public float getX() {
        return x;
    }

    public void setX(float x) {
        this.x = x;
    }

    public float getY() {
        return y;
    }

    public void setY(float y) {
        this.y = y;
    }
}
